     */
    public function store(Request $request)
    {
        $form = $request->all();
        DB::table('products')->insert([
            'title' => $form['title'],
            'content' => $form['content'],
            'price' => $form['price'],
            'quantity' => $form['quantity'],
            'created_at' => now(),
            'updated_at' => now(),
        ]);
        return response()->json(true);
    }

